import React, { useState } from "react";
import LayoutTeacher from "../../components/Layout/LayoutTeacher";

import Head from "next/head";

import { getSession, session, useSession } from "next-auth/client";

import Game from "../../components/Dashboard/Game";
import Games from "../../components/Dashboard/Games";
import NewGame from "../../components/Dashboard/NewGame";
import Students from "../../components/Dashboard/Students";

export default function index({ session }) {
  const teacher = session?.user?.user?.user.teacher;
  //console.log(session?.user?.user?.user.teacher);

  const [game, setGame] = useState(null);

  const [id, setId] = useState(1);

  const routes = [
    {
      id: 0,
      name: "Nieuwe game",
      component: <NewGame teacher={teacher} />,
    },
    {
      id: 1,
      name: "Gemaakte spellen",
      component: <Games setId={setId} setGame={setGame} teacher={teacher} />,
    },
    {
      id: 2,
      name: "Game pagina",
      component: <Game setId={setId} game={game} teacher={teacher} />,
    },
    {
      id: 3,
      name: "Students",
      component: <Students setId={setId} game={game} teacher={teacher} />,
    },
  ];

  return (
    <LayoutTeacher id={id} setId={setId}>
      <Head>
        <title>Leerkracht Dashboard - BuddyGram</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/smiley.png" />
      </Head>

      <div>{routes[id].component}</div>
    </LayoutTeacher>
  );
}

export async function getServerSideProps(ctx) {
  const session = await getSession(ctx);
  console.log(session?.user?.user?.id);
  if (!session?.user) {
    return {
      redirect: {
        destination: "/login",
        permanent: false,
      },
    };
  }
  return {
    props: {
      session: await getSession(ctx),
    },
  };
}
