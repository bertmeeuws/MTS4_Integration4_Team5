import React, { useState } from "react";
import LayoutTeacher from "../../components/Layout/LayoutTeacher";
import { v4 as uuid } from "uuid";
import axios from "axios";
import Head from "next/head";
import CustomCursor from "../../components/CustomCursor/CustomCursor";

export default function index() {
  const [link, setLink] = useState("");

  const handleGenerateLink = (e) => {
    e.preventDefault();

    try {
      axios
        .post("http://localhost:1337/links", {
          link_id: uuid(),
          user_permissions_user: 0,
        })
        .then((response) => {
          console.log(response.data);
          setLink(`http://localhost:3000/game/${response?.data?.link_id}`);
        });
    } catch (e) {
      console.log("Error: " + e);
    }
  };

  return (
    <LayoutTeacher>
      <Head>
        <title>Leerkracht Dashboard - BuddyGram</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <section className="wrapper">
        <p className="label-text">
          Genereer een link en deel deze met je studenten
        </p>
        <form className="form__generateLink" onSubmit={handleGenerateLink}>
          <input
            type="text"
            value={link}
            onChange={(e) => setLink(e.currentTarget.value)}
            className="input__generatedlink"
            readOnly
          />
          <input type="submit" value="Generate link" />
        </form>
      </section>
    </LayoutTeacher>
  );
}
